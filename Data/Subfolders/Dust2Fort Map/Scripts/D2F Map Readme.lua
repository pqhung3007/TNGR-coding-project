Dust2Fort Template Readme
Template created Aug 2021 by Utterly (Discord Utterly (Chris)#7762)

A battle arena map inspired by the iconic Counter Strike Dust2 map.
Template includes the map geometry and environment only. No gameplay mechanics, spawns, etc.
Layout should work with most game styles with lots of options for capture points and objectives.
Designed for first person camera. Untested in third person view.


USAGE:
Open a new project or existing project and remove the geometry and environment.
Import Dust2Fort template from Community Content.
Drag n Drop into hierarchy.
Create spawn points, gameplay, etc.

Alternatively the template is available in the dust2fort-showcase sample game (https://www.coregames.com/games/03bbd4/dust2fort-showcase) 
which is open for editing. This simple game uses the default Last Team Standing framework and includes a bonus default loadout template.  


To assist with customising the map all custom materials have been prefixed with D2Fâ€¦. (eg "D2F Concrete 01"). 

Map is divided into 14 zones with each zone containing one or more buildings. If rearranging objects be aware that some complex models (eg security bars over windows) have collision turned off and an invisible collision proxy object placed over them. Search hierarchy for "Collision Proxy" to find them. 

